                        <h2>Thế nào là nạn nhân của buôn người?</h2>
                        <p>Thảm kịch tại Anh vừa rồi cho thấy có những lao động trẻ sẵn sàng ra nước ngoài mà không hề tuân theo pháp luật về xuất nhập cảnh của VN cũng như nước sở tại. Có thể hiểu rằng họ chấp nhận rủi ro lớn để đánh đổi những ước mơ, hoài bão cũng lớn.</p>
                        <p>TTuy nhiên, những người đó đã bị chuyển từ nơi này sang nơi khác, từ nhóm người này sang nhóm khác trong môi trường và điều kiện khắc nghiệt quá mức bình thường, trong khi chính họ cũng không chắc sẽ phải đối mặt với cuộc sống ra sao tại điểm đến trong tình trạng người cư trú bất hợp pháp.</p>
                        <p>TTheo nghị định thư về phòng chống và trừng trị tội buôn người, đặc biệt là phụ nữ và trẻ em (nghị định thư Palermo), “buôn người” được định nghĩa là: “Việc tuyển dụng, vận chuyển, chuyển nhượng, chứa chấp hoặc tiếp nhận người, bằng biện pháp đe dọa hoặc sử dụng vũ lực hay các hình thức khác như ép buộc, bắt cóc, lừa gạt, lạm dụng quyền lực hoặc lợi dụng sự yếu thế, hoặc đưa hay nhận tiền hay lợi ích để đạt được sự đồng thuận của một người... vì mục đích bóc lột. Sự bóc lột bao gồm ở lạm dụng tình dục, các dạng bóc lột tình dục khác, lao động cưỡng bức hoặc như nô lệ, hoặc các hoạt động tương tự”.</p>
                        <p>39 nạn nhân xấu số đã tìm cách lọt vào Anh bất hợp pháp có thể đã trả rất nhiều tiền cho người môi giới, nhưng họ được thông tin không chính xác và chưa được biết những rủi ro có thể xảy ra với mình, bị vận chuyển trong môi trường bất trắc trong khi chưa biết sau này công việc, cuộc sống sẽ ra sao, như vậy, họ chính là nạn nhân của nạn buôn người.</p>
                        <p>Trong suốt hành trình, họ có thể đã bị lừa gạt hay bị lạm dụng vì ở thế yếu. Xét những thực tế đó, vấn nạn buôn người đang diễn ra với không ít người VN. Những người đã mạo hiểm tính mạng mình kia không phải là những người đầu tiên. Chắc chắn còn có những người VN khác đã trải qua một hành trình tương tự, được nghe những tin đồn về sự thành công, giàu có dễ dàng nơi đất khách.</p>
                        <p>Việc đưa người lao động ra nước ngoài là một việc có ý nghĩa và chính đáng, như đã trình bày ở trên. Nhưng trước hết, XKLĐ phải song hành với việc nhà nước biết bảo vệ quyền của người lao động và công dân nước mình. Hơn nữa, việc người lao động, công dân VN đi lao động nước ngoài phải tạo được tương lai sáng sủa hơn cũng như phát triển nghề nghiệp, hạnh phúc của họ. Đúng là ngày trước khi kinh tế VN còn khó khăn, nên đẩy mạnh XKLĐ để tạo thêm việc làm và góp phần phát triển đất nước là hợp lý.</p>
                        <p>Nhưng VN nay đã đổi mới được hơn 30 năm, chính sách công nghiệp hóa - hiện đại hóa đã được triển khai từ lâu. Phải chăng đã tới lúc chừng mực hơn với các chương trình XKLĐ hàng loạt? Nhà nước nên chăng có một kế hoạch và chính sách hiệu quả để chia sẻ thông điệp rằng người dân sẽ có công ăn việc làm tốt hơn, ổn định hơn ngay ở quê hương mình. Đi đôi với truyền thông, xây dựng mạng lưới an sinh xã hội trong nước cũng là trách nhiệm then chốt của Nhà nước để biến thông điệp đó thành hiện thực.</p>

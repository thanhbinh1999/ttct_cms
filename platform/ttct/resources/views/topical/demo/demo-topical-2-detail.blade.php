                        <p class="article-summary">TTCT - Xuất khẩu lao động (XKLĐ) đã được Nhà nước VN xúc tiến từ rất lâu. Từ những năm 1980, VN bắt đầu XKLĐ đi làm việc có thời hạn với hình thức chủ yếu là hợp tác sử dụng lao động thông qua các hiệp định chính phủ trực tiếp ký kết.</p>
                        <figure href="{{ asset('assets/article-preview/img/page/detail-1.jpg') }}" data-fancybox="gallery" data-caption="Nhiều người lao động đăng ký kỳ thi tuyển tiếng Hàn để tìm cơ hội được làm việc ở Hàn Quốc. Ảnh: Đ.BÌNH">
                            <img src="{{ asset('assets/article-preview/img/page/detail-1.jpg') }}" alt="">
                            <figcaption><span>Nhiều người lao động đăng ký kỳ thi tuyển tiếng Hàn để tìm cơ hội được làm việc ở Hàn Quốc. Ảnh: Đ.BÌNH</span></figcaption>
                        </figure>
                        <p>Không thể phủ nhận những kết quả mà XKLĐ mang lại cho VN đến nay. Thứ nhất là giải quyết vấn đề công ăn việc làm. Từ khi kinh tế VN còn rất nhiều khó khăn đến lúc đã có những bước phát triển rõ nét, luôn có khá nhiều lao động trẻ không có việc, không đủ việc để làm, hoặc những công việc thu nhập quá thấp, đặc biệt khi phần lớn dân số trẻ vẫn sinh ra và lớn lên ở vùng nông thôn. XKLĐ đã phần nào lấp vào khoảng trống, tạo ra công ăn việc làm cho nhiều người, góp phần đáng kể bảo đảm an sinh xã hội.</p>
                        <p>Thứ hai, đây là sự đóng góp quan trọng cho nguồn thu ngoại tệ của VN. Thu nhập của lao động VN ở nước ngoài gửi về nước bình quân mỗi năm trên 2 tỉ USD, theo thống kê của Ủy ban Các vấn đề xã hội của Quốc hội, cho khoảng thời gian 2010 - 2017, là một nguồn đóng góp đáng kể cho GDP đất nước, nhất là ở thời kỳ đầu còn khó khăn.</p>
                        <p>
                            Tuy nhiên, vẫn còn nhiều tồn tại cũng như các vấn đề xã hội xung quanh câu chuyện XKLĐ.</p>
                        <h2>Mù mờ thông tin</h2>
                        <p>Hiện trạng là người lao động VN muốn ra nước ngoài làm việc rất thiếu hụt thông tin và sự hướng dẫn minh bạch, chính xác và hợp lý. Phải biết rằng đi XKLĐ là một quyết định lớn của đời người, và để ra được quyết định chính xác thì cần phải đào xới cả một núi thông tin.</p>
                        <p>Nhưng tình trạng chung hiện nay là nhiều người lao động không những không biết và không phân biệt được công ty XKLĐ nào là đáng tin cậy, họ thậm chí còn không phân biệt được thông tin về cơ hội việc làm họ nhận được có chính xác hay không.</p>
                        <p>Phần lớn người đi XKLĐ, do không hoặc ít có kinh nghiệm đi nước ngoài, khá mù mờ về thông tin môi trường, điều kiện lao động tại nơi mình dự kiến làm việc. Các công ty XKLĐ đưa ra nhiều mức phí dịch vụ và tiền đặt cọc khác nhau, nhiều công ty đưa ra mức tiền lớn đến vài trăm triệu đồng, trong khi pháp luật liên quan đến vấn đề này có quy định mức trần các loại phí dịch vụ. Nhưng những thông tin này hầu như chưa được giới thiệu và phổ cập cho các người lao động.</p>
                        <p>Nếu được cung cấp thông tin chính xác và minh bạch, người lao động có thể cân nhắc đầy đủ và hợp lý bài toán cuộc đời của họ, tự xây dựng kế hoạch tài chính cũng như biết cách bảo vệ quyền lợi chính đáng trong hành trình “xuất dương”.</p>
                        <p>Thực tế hiện giờ không ít người lao động lựa chọn trong bối cảnh thông tin mù mờ như thế, dẫn tới việc XKLĐ không được như mong muốn, nhiều người còn hối hận sau khi đối mặt với thực tế phũ phàng.</p>
                        <p>Trong các vụ việc mà tôi và các cộng sự hỗ trợ (hoạt động tình nguyện không thu tiền) cho người lao động bị ngược đãi, đối xử không công bằng hay trái pháp luật, hầu như mọi trường hợp đều cho thấy những vấn đề nêu trên.</p>
                        <p>Theo cơ chế hiện nay, những người thực tế có thể gặp và liên hệ trực tiếp với người lao động chính là các công ty XKLĐ được cấp phép qua Bộ Lao động, thương binh và xã hội. Đó cũng là đối tượng chính đóng vai trò bảo vệ quyền và lợi ích hợp pháp của người lao động xuất khẩu. Đáng lẽ các công ty này phải coi mình là chủ thể cung cấp một dịch vụ công cho công dân VN, thay mặt cho Nhà nước và xã hội, qua đó phải hỗ trợ và bảo vệ công dân VN thực hiện quyền mưu cầu hạnh phúc chính đáng.</p>
                        <p>Từ góc nhìn đó, Bộ Lao động, thương binh và xã hội, cụ thể hơn là Cục Lao động ngoài nước, nên tăng cường khả năng giám sát, kiểm soát cụ thể, chi tiết, mạnh mẽ hơn trước tình trạng hoạt động thực tế của những công ty XKLĐ còn nhiều bất cập, có các biện pháp chế tài hiệu quả hơn để chấn chỉnh hoạt động của các công ty này.</p>
                        <p>Thực trạng hoạt động đó bao gồm một khúc mắc lớn trong dịch vụ XKLĐ hiện nay: vai trò người môi giới. Pháp luật hiện hành không cấm sự tồn tại của môi giới. Vấn đề ở chỗ khác với các công ty XKLĐ, vốn là pháp nhân phải trải qua quá trình xem xét về tư cách hoạt động và được cấp phép thì mới được hoạt động, những cá nhân - tổ chức làm môi giới không cần giấy phép, không được kiểm tra chất lượng cũng như khả năng thực hiện trách nhiệm xã hội với người lao động khi có sự cố.</p>
                        <p>Rất nhiều đơn vị môi giới đưa thông tin không rõ ràng, thậm chí có hành vi không trung thực, lừa đảo. XKLĐ không phải là giao dịch hàng hóa. Nếu Nhà nước đã quy định và chế định chặt chẽ với các công ty XKLĐ thì không có lý do gì để người môi giới vẫn tồn tại.</p>
